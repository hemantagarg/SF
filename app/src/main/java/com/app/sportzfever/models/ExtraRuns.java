public class ExtraRuns {

    private String b;

    private String nb;

    private String lb;

    private String wd;

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getNb() {
        return nb;
    }

    public void setNb(String nb) {
        this.nb = nb;
    }

    public String getLb() {
        return lb;
    }

    public void setLb(String lb) {
        this.lb = lb;
    }

    public String getWd() {
        return wd;
    }

    public void setWd(String wd) {
        this.wd = wd;
    }

}
